// Bubble Sort
// Repeatedly steps through the list, compares adjacent elements and swaps them if they are 
// in the wrong order. The pass through the list is repeated until the list is sorted.
// This is a stable sorting algorithm.

// Insertion Sort
// The algorithm builds the final sorted array one element at a time. It takes an element from 
// the unsorted part of the array and places it in its correct position in the sorted part.

// Quick Sort
// Divides the array into two smaller sub-arrays (low elements and high elements), recursively 
// sorts the sub-arrays. Commonly implemented with the "divide and conquer" 

// Merge Sort
// Divides the array into two halves, recursively sorts them, and then merges the two sorted 
// halves. It is an example of the divide and conquer strategy.

