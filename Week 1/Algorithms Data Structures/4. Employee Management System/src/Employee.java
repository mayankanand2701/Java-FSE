// Arrays are a collection of elements stored in contiguous memory locations. This means that 
// each element in the array is located next to the previous element in the system's memory.
// The memory address of the first element is used to calculate the addresses of subsequent 
// elements.

// Advantages of Arrays
// 1.Arrays allow for direct access to any element using its index. 
// 2.Iterating through an array is efficient because the elements are stored in contiguous 
//   memory locations.
// 3.The size of an array is fixed upon creation, which makes memory usage predictable. 
// 4.Arrays are simple and straightforward to use.

